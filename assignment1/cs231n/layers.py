    true_scores = x[np.arange(len(y)),y]
    difference = (x.T - true_scores).T
    ind = ((difference + 1 )>0)*1
    ind[np.arange(len(y)),y] = 0
    loss = np.sum((x.T - true_scores + 1).T[ind>0])
    dx =  ind
